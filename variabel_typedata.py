# Variabel dan Tipe Data Dasar

# 1. Tipe Data Integer (Bilangan Bulat)
jumlah_siswa = 25
print("Jumlah siswa:", jumlah_siswa, type(jumlah_siswa))

# 2. Tipe Data Float (Bilangan Desimal)
nilai_rata_rata = 85.5
print("Nilai rata-rata:", nilai_rata_rata, type(nilai_rata_rata))

# 3. Tipe Data String (Teks)
nama_siswa = "Budi Santoso"
print("Nama siswa:", nama_siswa, type(nama_siswa))

# 4. Tipe Data Boolean (Benar/Salah)
siswa_lulus = True
print("Siswa lulus:", siswa_lulus, type(siswa_lulus))

# 5. Tipe Data List (Daftar)
daftar_nilai = [90, 85, 78, 92]
print("Daftar nilai:", daftar_nilai, type(daftar_nilai))

# 6. Tipe Data Tuple (Tuple)
koordinat = (10, 20)
print("Koordinat:", koordinat, type(koordinat))

# 7. Tipe Data Dictionary (Kamus)
data_siswa = {"nama": "Siti Aminah", "usia": 17, "kota": "Jakarta"}
print("Data siswa:", data_siswa, type(data_siswa))

# Penjelasan:

# Variabel
# Variabel adalah tempat untuk menyimpan data. Dalam Python, Anda tidak perlu mendeklarasikan tipe data variabel secara eksplisit. Python akan menentukan tipe data berdasarkan nilai yang diberikan.

# Tipe Data
# Integer (int): Bilangan bulat (misalnya, 10, -5, 0).
# Float (float): Bilangan desimal (misalnya, 3.14, -2.5).
# String (str): Teks (misalnya, "Halo", 'Python').
# Boolean (bool): Nilai kebenaran (True atau False).
# List (list): Daftar nilai yang dapat diubah.
# Tuple (tuple): Daftar nilai yang tidak dapat diubah.
# Dictionary (dict): Kumpulan pasangan kunci-nilai.

# Fungsi type()
# Fungsi type() digunakan untuk mengetahui tipe data suatu variabel.

